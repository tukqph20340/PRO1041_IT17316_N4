    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        CuaHang x = new CuaHang();
        x.setMaCH(txtMaCH.getText());
        x.setTen(txtTen.getText());
        x.setDiaChi(txtDiaChi.getText());
        x.setThanhPho(txtThanhPho.getText());
        x.setQuocGia(txtQuocGia.getText());
        try {
            if (txtMaCH.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Mã Cửa Hàng không được để trống");
                return;
            }
            /*for (String c : service.getListMaCH()) {
                if (txtMaCH.getText().equals(c) == true) {
                    JOptionPane.showMessageDialog(this, "Mã Cửa Hàng đã được sử dụng");
                    return;
                }
            }*/
            if (checkMa()) {
                JOptionPane.showMessageDialog(this, "Mã Cửa Hàng đã được sử dụng");
                return;
            }
            if (txtTen.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Tên Cửa Hàng không được để trống");
                return;
            }
            if (txtDiaChi.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Địa Chỉ không được để trống");
                return;
            }
            if (txtThanhPho.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Thành Phố không được để trống");
                return;
            }
            if (txtQuocGia.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Quốc Gia không được để trống");
                return;
            }
            if (service.them(x)) {
                loadTable(service.getList());
                JOptionPane.showMessageDialog(this, "Thêm Thành Công");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnThemActionPerformed

