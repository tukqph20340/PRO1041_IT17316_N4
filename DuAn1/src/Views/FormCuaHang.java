    public void loadTable(ArrayList<CuaHangViewModel> list) {
        model = (DefaultTableModel) tblQLCH.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new String[]{"Mã cửa hàng", "Tên", "Địa chỉ", "Thành Phố", "Quốc gia"});
        for (CuaHangViewModel x : list) {
            model.addRow(new Object[]{x.getMaCH(), x.getTen(), x.getDiaChi(), x.getThanhPho(), x.getQuocGia()});
        }
    }
    
